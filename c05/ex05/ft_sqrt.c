int	ft_sqrt(int nb)
{
	int max;

	max = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb > 2147395600)
		return (0);
	while (nb > max * max)
		max++;
	if (nb == max * max)
		return (max);
	else
		return (0);
}
