#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*pt;

	i = 0;
	if (argc >= 1)
	{
		pt = argv[0];
		while (pt[i] != '\0')
		{
			write(1, &pt[i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
