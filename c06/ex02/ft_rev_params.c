#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*pt;

	while (argc - 1 > 0)
	{
		pt = argv[argc - 1];
		i = 0;
		while (pt[i] != '\0')
		{
			write(1, &pt[i], 1);
			i++;
		}
		write(1, "\n", 1);
		argc--;
	}
	return (0);
}
