#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	int		j;
	char	*pt;

	j = 1;
	while (j < argc)
	{
		pt = argv[j];
		i = 0;
		while (pt[i] != '\0')
		{
			write(1, &pt[i], 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
	return (0);
}
